					<button class="ps-button">登录</button>
				</view>
			</uni-transition>
		</view>
	</view>
</template>

<script>
	export default {
		data() {
			return {
				user: {
					imageSrc: "../../static/dog.jpg",
					nickName: "登录/注册",
					phone: "请绑定手机"
				},
				settings: [{
					index: 0,
					msg: '绑定手机',
					icon: 'contact'
				}, {
					index: 1,
					msg: '学生认证',
					icon: 'checkmarkempty'
				}, {
					index: 2,
					msg: '闲置收藏',
					icon: 'heart-filled'
				}, {
					index: 3,
					msg: '意见反馈',
					icon: 'info-filled'
				}, {
					index: 4,
					msg: '联系客服',
					icon: 'chat-filled'
				}, ],
				isPop: false,
				showTelCheck: false,
				showStuCheck: false,
				isEndAnimation: false,
			};
		},
		methods: {
			checkTelephone() {
				this.showTelCheck = true
				this.isPop = true
			},
			checkStudent() {
				this.showStuCheck = true
				this.isPop = true
			},
			settingItemClick(index) {
				switch (index) {
					//	绑定手机
					case 0:
						this.checkTelephone()
						break
						//	绑定学号 
					case 1:
						this.checkStudent()
						break
					default:
						return
				}
			},
			popDown() {
				this.showTelCheck = false
				this.showStuCheck = false
			},
			login() {
				uni.getUserProfile({
					desc: '获取你的昵称、头像、地区及性别',
					success: res => {
						this.user.imageSrc = res.userInfo.avatarUrl
						this.user.nickName = res.userInfo.nickName
					},
					fail: res => {
						uni.showToast({
							title: '您拒绝了请求:D',
							icon: 'error',
							duration: 2000
						});
						return;
					}
				});
			},
			animationEnd(e) {
				if (this.isEndAnimation) {
					this.isPop = false
				}
				this.isEndAnimation = !this.isEndAnimation
			}
		}
	}
</script>

